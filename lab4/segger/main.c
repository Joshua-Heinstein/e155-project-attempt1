void playSong(const int song[][2]) {
    int i = 0;
    
    // Play notes until we hit the end marker (frequency = 0, duration = 0)
    while (!(song[i][0] == 0 && song[i][1] == 0)) {
        playNote(song[i][0], song[i][1]);
        i++;
        }
  stopNote();
}


